package aslib.net;

/**
 * <p>Contains the options to represent the statuses of an internet connection.</p>
 *
 * @author Adriano Siqueira
 * @version 4.0.0
 * @since 6.1.0
 */
public enum ConnectionStatus {

    /**
     * <p>Indicates that the machine has no internet connection.</p>
     *
     * @since 1.0.0
     */
    OFFLINE,

    /**
     * <p>Indicates that the machine has an internet connection.</p>
     *
     * @since 1.0.0
     */
    ONLINE
}
